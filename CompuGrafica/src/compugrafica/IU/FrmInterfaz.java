    private void zoomTapete() {

        Tapete1 a = new Tapete1();
        Tapete1.setTipo(cbxTapetes.getSelectedIndex());
        a.encender(AreaTrabajo.getLienzo());
        AreaTrabajo.pintar();

        int n = 2;
        Vector v2 = new Vector(x0, y0);

        Cuadrado c = new Cuadrado(v2.x0 - 0.75 / n, v2.y0 - 0.625 / n, v2.x0 + 0.75 / n, v2.y0 + 0.625 / n);

        a.Rx1 = v2.x0 - 0.75 / n;
        a.Rx2 = v2.x0 + 0.75 / n;
        a.Ry1 = v2.y0 - 0.625 / n;
        a.Ry2 = v2.y0 + 0.625 / n;

        c.encender(AreaTrabajo.getLienzo());
        AreaTrabajo.pintar();
        timer(1500);
        a.encender(AreaTrabajo.getLienzo());
        AreaTrabajo.pintar();
    }

