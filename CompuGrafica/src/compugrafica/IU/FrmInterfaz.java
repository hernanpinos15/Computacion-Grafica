    //ADD MOUSE LISTENERS
    private void areaTrabajoAddMouseListeners() {

        AreaTrabajo.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent evt) {
                x0 = evt.getX();
                y0 = evt.getY();
                arrayX.add(x0);
                arrayY.add(y0);
                Vector prueba = new Vector();
                prueba.procesoCarta(x0, y0);
                arrayXD.add(prueba.getSx());
                arrayYD.add(prueba.getSy());
                VxPro.add(prueba.getSx());
                VyPro.add(prueba.getSy());

            }
        });

        AreaTrabajo.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseReleased(MouseEvent evt) {
                xf = evt.getX();
                yf = evt.getY();
                Rd = Math.sqrt((Math.pow(x0 - xf, 2)) + (Math.pow(y0 - yf, 2)));

                areaTrabajoMouseReleased();
            }
        });

        AreaTrabajo.addMouseMotionListener(new MouseAdapter() {
            @Override
            public void mouseMoved(MouseEvent evt) {
                lblxreal.setText("");
                lblyreal.setText("");
                lblxcarta.setText("");
                lblycarta.setText("");

                Ventanas vent = new Ventanas();
                vent.procesoCarta(evt.getX(), evt.getY());

                Double dx = vent.getSx();
                Double dy = vent.getSy();

                String x = String.format("%.2f", dx);
                String y = String.format("%.2f", dy);

                lblxreal.setText(String.format("[%d]", evt.getX()));
                lblyreal.setText(String.format("[%d]", evt.getY()));

                lblxcarta.setText(x);
                lblycarta.setText(y);

            }
        });
    }

