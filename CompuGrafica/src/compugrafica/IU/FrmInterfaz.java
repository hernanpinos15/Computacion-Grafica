    private void jButton24ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton24ActionPerformed

//        AreaTrabajoProyecto.limpiar();
        //Llamado a la CURVA DE BEZIER
        Bezier bz = new Bezier(VxPro, VyPro);
        bz.color = color;
        bz.encender(AreaTrabajo.getLienzo());
        AreaTrabajo.pintar();

        //LLamado a la curva de superficie de revolucion
        Proyecto pro = new Proyecto(bz.BezierX, bz.BezierY);
        pro.color = colorPro;
        pro.EjeSuperficie = cbxEjeRotacion.getSelectedIndex();//EJE DE GENERACION DE SUPERFICIE
        pro.setTipoRotar(cbxRotarSR.getSelectedIndex());
        pro.OpcionRelleno = cbxRellenoPro.getSelectedIndex();
        double w = 0;
        double dw = 0.2;
        int eje = 1;
        if (cbxRotarSR.getSelectedIndex() == 0) {
            pro.encender(AreaTrabajoProyecto.getLienzo());
            AreaTrabajoProyecto.pintar();
        } else {
            if (cbxRotarSR.getSelectedIndex() == 4) {
                do {
                    w = 0;
                    do {
//                        pro.tetaSR = w;
                        if (eje == 1) {
                            pro.teta1 = w;
                        }

                        if (eje == 2) {
                            pro.teta2 = w;
                        }

                        if (eje == 3) {
                            pro.teta3 = w;
                        }
                        pro.encender(AreaTrabajoProyecto.getLienzo());
                        AreaTrabajoProyecto.pintar();
                        timer(100);
                        pro.apagar(AreaTrabajoProyecto.getLienzo());
                        w += dw;
                    } while (w <= Math.PI * 2);
                    eje = eje + 1;
                } while (eje <= 3);
            } else {
                do {

//                    pro.tetaSR = w;
                    if (cbxRotarSR.getSelectedIndex() == 1) {
                        pro.teta1 = w;
                    }

                    if (cbxRotarSR.getSelectedIndex() == 2) {
                        pro.teta2 = w;
                    }

                    if (cbxRotarSR.getSelectedIndex() == 3) {
                        pro.teta3 = w;
                    }

                    pro.encender(AreaTrabajoProyecto.getLienzo());
                    AreaTrabajoProyecto.pintar();
                    timer(100);
                    pro.apagar(AreaTrabajoProyecto.getLienzo());
                    w += dw;
                } while (w <= Math.PI * 2);
            }
        }


    }//GEN-LAST:event_jButton24ActionPerformed

